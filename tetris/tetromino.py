# 方块颜色 (RGB)
TETROMINO_COLORS = {
    'I': (0, 255, 255),    # 青色
    'O': (255, 255, 0),    # 黄色
    'T': (128, 0, 128),    # 紫色
    'S': (0, 255, 0),      # 绿色
    'Z': (255, 0, 0),      # 红色
    'J': (0, 0, 255),      # 蓝色
    'L': (255, 165, 0),    # 橙色
}


class Tetromino:
    """方块类"""

    def __init__(self, shape_type: str):
        self.type = shape_type
        self.shape = TETROMINO_SHAPES[shape_type]
        self.color = TETROMINO_COLORS[shape_type]
        self.rotation = 0  # 0-3
        self.x = 0
        self.y = 0
        logger.debug(f"创建方块: {shape_type}")

    def get_current_shape(self) -> List[List[int]]:
        """获取当前旋转状态的形状"""
        return self.shape[self.rotation]

    def rotate(self) -> None:
        """旋转方块"""
        self.rotation = (self.rotation + 1) % 4
        logger.debug(f"方块 {self.type} 旋转到状态 {self.rotation}")

    def get_blocks(self) -> List[Tuple[int, int]]:
        """获取方块所有方格的位置"""
        blocks = []
        shape = self.get_current_shape()
        for row in range(4):
            for col in range(4):
                if shape[row][col]:
                    blocks.append((self.x + col, self.y + row))
        return blocks